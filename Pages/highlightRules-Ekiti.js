function checkCondition(row, headers, model, threshold) {
  const makeModelIndex = headers.indexOf('MAKEMODEL');
  const taIndex = headers.indexOf('TRANSACTIONAMOUNT');
  if (makeModelIndex === -1 || taIndex === -1) return false;
  const makemodel = (row[makeModelIndex] ?? '').toString().toLowerCase().replace(/\s+/g, '');
  const amount = parseFloat(row[taIndex]);
  return !isNaN(amount) && makemodel === model && amount < threshold && amount !== 200;
}
